using System;  // Importa o namespace 'System', que contém classes básicas do .NET, 
               // como Console, Math, String, DateTime, etc.

namespace Namespaces  // Define um namespace chamado 'Namespaces' para agrupar classes relacionadas
{
    // Classe chamada 'Program' dentro do namespace 'Namespaces'
    class Program
    {
        // Método principal (ponto de entrada do programa)
        static void Main(string[] args)
        {
            // Código principal do programa vai aqui
            Console.WriteLine("Namespaces são mão na roda!");
        }
    }
}
